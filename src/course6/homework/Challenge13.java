public class Challenge13 {

    public static void main(String[] args) {

        int number = 0;

        System.out.println("The original number is: " + number);
        number++;
        System.out.println("After using ++ it becomes: " + number);
        number--;
        System.out.println("After using -- on the new value, it becomes: " + number);
    }
}
